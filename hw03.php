Задача 3
Написать функцию - конвертер строки.
Возможности:
перевод всех символов в верхний регистр,
перевод всех символов в нижний регистр,
перевод всех символов в нижний регистр и первых символов слов в верхний регистр.
Это должна быть одна функция
*/

function str_convert ($str, $converter) {
    if ( is_string($str) ) {
        switch ($converter) {
            case "upper": 
                return mb_strtoupper($str);
            case "lower": 
                return mb_strtolower($str);
            case "capitalize":
                $str = mb_strtolower($str);
                $str = ucwords($str);
                return($str);
            default:
                return "Конвертер это пока не умеет";
        }
    } else {
        return "Это не строка";
    }
}
echo str_convert ("перевод всех символов в верхний регистр", 'upper');
echo str_convert ("ПЕРЕВОД ВСЕХ СИМВОЛОВ В НИЖНИЙ РЕГИСТР", 'lower');
echo str_convert ("Once upon a time", 'capitalize');
